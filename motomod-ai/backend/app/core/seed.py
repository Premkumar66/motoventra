async def seed_data():
    async with AsyncSessionLocal() as session:
        brand_map = {}
        brands_data = set(b["brand"] for b in BIKES_AND_VARIANTS_SEED)
        for b_name in brands_data:
            slug = b_name.lower().replace(" ", "-")
            res = await session.execute(select(Brand).where(Brand.slug == slug))
            existing = res.scalar_one_or_none()
            if not existing:
                brand_obj = Brand(id=uuid.uuid4(), name=b_name.replace("-", " ").title(), slug=slug, country="Global", is_active=True)
                session.add(brand_obj)
                await session.flush()
                brand_map[slug] = brand_obj.id
            else:
                brand_map[slug] = existing.id

        for bike in BIKES_AND_VARIANTS_SEED:
            b_slug = bike["brand"]
            m_slug = bike["slug"]
            brand_id = brand_map.get(b_slug)
            if not brand_id:
                continue
            res = await session.execute(select(Motorcycle).where(Motorcycle.slug == m_slug))
            existing_m = res.scalar_one_or_none()
            if not existing_m:
                m_obj = Motorcycle(id=uuid.uuid4(), brand_id=brand_id, name=bike["name"], slug=m_slug, category=bike["category"], is_active=True)
                session.add(m_obj)
                await session.flush()
                v_obj = MotorcycleVariant(id=uuid.uuid4(), motorcycle_id=m_obj.id, variant_name=bike.get("variant", "Standard"), slug=f"{m_slug}-std", year=bike["year"], price_inr=bike["price"], engine_cc=bike["cc"], horsepower_bhp=bike["hp"], torque_nm=bike["torque"], fuel_tank_liters=bike["tank"], mileage_kmpl=bike["kmpl"], top_speed_kmh=bike["speed"], weight_kg=165.0, seat_height_mm=800.0, ground_clearance_mm=160.0, has_abs=bike["abs"], fuel_type=FuelType.PETROL, cooling=CoolingType.LIQUID, transmission=TransmissionType.MANUAL, emission_standard=EmissionStandard.BS6, is_active=True)
                session.add(v_obj)
        # 3. Seed Default Users
        from app.models.user import User, UserRole, UserStatus
        from app.core.security import hash_password

        res_admin = await session.execute(select(User).where(User.email == "admin@motomod.ai"))
        if not res_admin.scalar_one_or_none():
            admin_user = User(
                id=uuid.uuid4(),
                email="admin@motomod.ai",
                username="admin",
                full_name="Platform Admin",
                hashed_password=hash_password("Admin@MotoMod2024!"),
                role=UserRole.ADMIN,
                status=UserStatus.ACTIVE,
                is_email_verified=True,
                is_active=True,
                is_superuser=True
            )
            session.add(admin_user)

        res_user = await session.execute(select(User).where(User.email == "user@motomod.ai"))
        if not res_user.scalar_one_or_none():
            demo_user = User(
                id=uuid.uuid4(),
                email="user@motomod.ai",
                username="demo_user",
                full_name="Demo Rider",
                hashed_password=hash_password("User@MotoMod2024!"),
                role=UserRole.USER,
                status=UserStatus.ACTIVE,
                is_email_verified=True,
                is_active=True
            )
            session.add(demo_user)

        # 4. Seed Default Licenses & Sources
        from app.models.image_dataset import ImageLicense, ImageSource, LicenseType, ImportSource

        res_lic = await session.execute(select(ImageLicense).where(ImageLicense.spdx_id == "CC-BY-4.0"))
        if not res_lic.scalar_one_or_none():
            cc_by = ImageLicense(
                id=uuid.uuid4(),
                name="Creative Commons Attribution 4.0 International",
                spdx_id="CC-BY-4.0",
                license_type=LicenseType.CC_BY,
                allows_commercial_use=True,
                requires_attribution=True,
                url="https://creativecommons.org/licenses/by/4.0/"
            )
            session.add(cc_by)

        res_src = await session.execute(select(ImageSource).where(ImageSource.name == "Wikimedia Commons"))
        if not res_src.scalar_one_or_none():
            wiki_src = ImageSource(
                id=uuid.uuid4(),
                name="Wikimedia Commons",
                source_type=ImportSource.WIKIMEDIA,
                source_url="https://commons.wikimedia.org",
                allows_automated_collection=True,
                attribution_template="File from Wikimedia Commons (CC-BY-SA)"
            )
            session.add(wiki_src)

        await session.commit()
        print(f"Seeded {len(BIKES_AND_VARIANTS_SEED)} models, users, licenses, and sources successfully!")

    # Phase 5: Seed Modification Parts with strict per-bike compatibility
    from app.core.seed_modifications import seed_modification_parts
    await seed_modification_parts(session)

